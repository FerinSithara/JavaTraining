package calculator;

public class comMethods {

	
     // Methods will do some actions
	
	public int addition (int a, int b)
	{
		int c = a + b;
		return c;
	
	}
	
	protected int addition (int a, int b, int d)
	{
		int c = a + b + d;
		return c;
	
	}
	
	public int addition (int a, int b, int d, int e)
	{
		int c = a + b + d + e;
		return c;
	
	}
	
	public int subtraction (int d, int e)
	{
		int f = d - e;
		return f;
	
	}
	
	public int subtraction (int d, int e, int a)
	{
		int f = d - e - a;
		return f;
	
	}
	
	public int subtraction (int d, int e, int a, int b)
	{
		int f = d - e - a - b;
		return f;
	
	}
	
	public double multiplication (double g, double h)
	{
		double t = g * h;
		return t;
	
	}
	
	public double multiplication (double g, double h, double u)
	{
		double t = g * h * u;
		return t;
	
	}
	
	public double multiplication (double g, double h, double u, double v)
	{
		double t = g * h * u * v;
		return t;
	
	}
	
	public double division (double u, double v)
	{
		double w = u / v;
		return w;
	
	}
	
	public double division (double u, double v, double g)
	{
		double w = u / v / g;
		return w;
	
	}
	
	public double division (double u, double v, double h, double g)
	{
		double w = u / v / h / g;
		return w;
	
	}
}
